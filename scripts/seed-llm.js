// ---------------------------------------------------------------------------
function run() {
  const records = readJSON(`${ROOT}/data/records.json`);
  const bases = new Map(records.filter((r) => r.base === null).map((r) => [r.char, r]));

  // Resolve a char with FE0F tolerance so authored chars can never silently
  // miss the dataset spelling.
  function resolve(char) {
    if (bases.has(char)) return char;
    const plus = `${char}️`;
    if (bases.has(plus)) return plus;
    const minus = char.replace(/️$/u, "");
    if (bases.has(minus)) return minus;
    throw new Error(`char not found among base emojis: ${JSON.stringify(char)}`);
  }

  const summary = [];
  const resolvedByFacetTag = {};
  for (const [facet, tags] of Object.entries(PROPOSALS)) {
    const store = {};
    let count = 0;
    for (const [tag, entries] of Object.entries(tags)) {
      const seen = new Set();
      for (const [char, confidence] of entries) {
        if (!(confidence > 0 && confidence <= 1)) {
          throw new Error(`bad confidence for ${char} ${facet}:${tag}`);
        }
        const key = resolve(char);
        if (seen.has(key)) throw new Error(`duplicate ${key} in ${facet}:${tag}`);
        seen.add(key);
        (store[key] ||= []).push(makeTag(tag, facet, confidence, "llm"));
        (resolvedByFacetTag[`${facet}:${tag}`] ||= new Map()).set(char, key);
        count += 1;
      }
    }
    writeCharKeyedJSON(`${ROOT}/data/llm/${facet}.json`, store);
    summary.push([facet, Object.keys(store).length, count]);
  }

  // Merge safety decisions into data/reviews.json, keyed by the resolved char.
  const reviewsPath = `${ROOT}/data/reviews.json`;
  const reviews = existsSync(reviewsPath) ? readJSON(reviewsPath) : {};
  let decisions = 0;
  for (const [tag, { approved, rejected }] of Object.entries(SAFETY_DECISIONS)) {
    const map = resolvedByFacetTag[`safety:${tag}`] || new Map();
    const proposed = new Set(map.keys());
    for (const list of [approved, rejected]) {
      for (const char of list) {
        if (!proposed.has(char)) {
          throw new Error(`decision for ${char} safety:${tag} has no matching proposal`);
        }
      }
    }
    if (approved.length + rejected.length !== proposed.size) {
      throw new Error(`safety:${tag} has undecided proposals`);
    }
    for (const char of approved) {
      const key = map.get(char);
      (reviews[key] ||= {})[`safety::${tag}`] = "approved";
      decisions += 1;
    }
    for (const char of rejected) {
      const key = map.get(char);
      (reviews[key] ||= {})[`safety::${tag}`] = "rejected";
      decisions += 1;
    }
  }
  writeCharKeyedJSON(reviewsPath, reviews);

  log("SEED LLM PROPOSALS complete");
  for (const [facet, emojis, tags] of summary) {
    log(`  ${facet.padEnd(8)} ${String(emojis).padStart(4)} emojis, ${tags} tags`);
  }
  log(`  safety review decisions recorded  ${decisions}`);
  log("  wrote    data/llm/{vibe,object,domain,safety}.json, data/reviews.json");
}

run();
